package Figures.Figure;

public class Hexagon {
  protected int a;
  protected int b;
  protected int c;
  protected int d;
  protected int e;
  protected int g;
  protected String name;

  public Hexagon(){};

  public int getA() {
    return a;
  }

  public int getB() {
    return b;
  }

  public int getC() {
    return c;
  }

  public int getD() {
    return d;
  }

  public int getE() {
    return e;
  }

  public int getG() {
    return g;
  }

  public String getName() {
    return name;
  }

  public void setA(int a) {
    this.a = a;
  }

  public void setB(int b) {
    this.b = b;
  }

  public void setC(int c) {
    this.c = c;
  }

  public void setD(int d) {
    this.d = d;
  }

  public void setE(int e) {
    this.e = e;
  }

  public void setG(int g) {
    this.g = g;
  }

  public void setName(String name) {
    this.name = name;
  }
}
