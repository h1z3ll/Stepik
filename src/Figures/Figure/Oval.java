package Figures.Figure;

public class Oval {
  protected int radiusOx;
  protected int radiusOy;

  public Oval(){};

  public int getRadiusOx() {
    return radiusOx;
  }

  public int getRadiusOy() {
    return radiusOy;
  }

  public void setRadiusOx(int radiusOx) {
    this.radiusOx = radiusOx;
  }

  public void setRadiusOy(int radiusOy) {
    this.radiusOy = radiusOy;
  }
}
